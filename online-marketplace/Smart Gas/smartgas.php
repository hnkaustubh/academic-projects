		<p>Technological solutions called &#34smart wells&#34 and &#34smart fields&#34 have been applied in petroleum industry for nearly two decades. They aim to improve the knowledge of petroleum production processes, and thereby improve the efficiency of operations. Researchers and companies in pursuit of their goals tend to use one-sided implications and numerous synonyms when describing the substance of the term, which leads to an occasional confusion. In order to study the concept of smart field from a general perspective, a literature review has been conducted, and main characteristic attributes of such solutions have been revealed. Selected marketed products offered by engineering companies have been analyzed as examples of the technology implementation. A definition has been proposed along with its practical implications. Additionally, an attempt has been made to place these solutions in a broad scientific context of intelligence and sustainability in contemporary business processes.</p>
		<p>The adoption of smart gas meters in Europe is picking up speed. This year alone, the installed base of remotely read gas meters for consumers nearly doubled to 17.3 million units in EU28+2. The growth is mainly driven by nationwide rollouts in France, Italy, the Netherlands and the UK.

A variety of communication technologies are in use for the rollouts, including fixed RF networks, mobile networks and local interfaces to master electricity meters.

As next generation IoT-optimized 4G/5G mobile technologies become more widely available, it is expected that there will be a shift from legacy RF platforms to emerging standards like NB-IoT.

Italy was the first European country where smart gas meter installations began on a large scale. The national Regulatory Authority for Electricity, Gas and Water has set a target that 60% of residential gas meters and 100% of industrial gas meters should be smart by 2018 and full coverage by 2020.

The policy is based on a cost benefit analysis which concluded that there would be an economic benefit for gas DSOs with 50,000 customers or more to introduce smart meters for a total of 22 million customers. As a result, deployments started to take off in Italy during 2014.</p>
